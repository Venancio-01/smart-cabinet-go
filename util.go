// generateCRC16Code generates the CRC16 code for a given command.
func generateCRC16Code(command string) (string, error) {
	buffer, err := hex.DecodeString(command)
	if err != nil {
		return "", err
	}

	var wCRC uint16
	for _, chChar := range buffer {
		wCRC = (wCRC << 8) ^ CRC_CCITT_table[((wCRC>>8)^uint16(chChar))&0xff]
	}

	return fmt.Sprintf("%04x", wCRC), nil
}

// 解析RFID报告数据
func parseRFIDReportData(data string) []string {
	// 前缀
	const PREFIX = "5a00011200"
	// 使用前缀分割字符串
	arr := strings.Split(data, PREFIX)
	// 初始化结果数组
	var parseArr []string
	// 遍历分割后的字符串数组
	for _, cur := range arr {
		// 如果字符串以 "00" 开头
		if strings.HasPrefix(cur, "00") {
			// 计算字符串长度
			length, _ := strconv.ParseInt(cur[:4], 16, 64)
			// 将处理后的字符串放入结果数组中
			parseArr = append(parseArr, PREFIX+cur[:4+length*2])
		}
	}
	// 返回处理后的字符串数组
	return parseArr
}

// 根据报告数据获取TID
func getTIDByReportData(data string) string {
	// 前缀字符串
	const PREFIX = "5a00011200"
	// 正则表达式，用于提取EPC长度和TID长度
	re := regexp.MustCompile(PREFIX + "(.{4})(.{4})(.{16})(.{4})(.*)")
	// 匹配正则表达式
	matches := re.FindStringSubmatch(data)
	if len(matches) < 6 {
		return ""
	}
	// 解析EPC长度和TID长度
	tidLength, _ := strconv.ParseInt(matches[4], 16, 64)
	// 获取TID
	tid := matches[5][:tidLength*2]
	// 返回TID
	return tid
}

// 生成天线命令
func generateAntennaCommand() string {
	antennaIds := []int{1, 2, 3, 4}

	binary := generateBinaryString(antennaIds)
	command := binaryToHex(binary)

	return command
}
