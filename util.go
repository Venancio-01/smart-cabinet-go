package main

import (
	"encoding/hex"
	"fmt"
	"regexp"
	"strconv"
	"strings"
)

func generateBinaryString(numbers []int) string {
	const size = 32
	binaryArray := make([]string, size)

	for i := 0; i < size; i++ {
		binaryArray[i] = "0"
	}

	for _, num := range numbers {
		binaryArray[num-1] = "1"
	}

	binaryString := []string(nil)

	binaryString = append(binaryString, binaryArray...)

	return strings.Join(binaryString, "")
}

// Convert binary string to hexadecimal string
func binaryToHex(binary string) string {
	decimal, err := strconv.ParseInt(binary, 2, 64)
	if err != nil {
		return "00000000"
	}

	hex := fmt.Sprintf("%08X", decimal)

	return hex
}

